#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char const	*str;
	size_t				i;

	str = s;
	i = 0;
	while (i < n)
	{
		if (str[i] == (unsigned char)c)
			return ((void*)&s[i]);
		i++;
	}
	return (NULL);
}
